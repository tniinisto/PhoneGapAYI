
var serviceURL = "https://dev-areyouin.azurewebsites.net/pgmobile/services/";

//Forgotten password/////////////////////////////////////////////////////
function forgotPassword(mail) {
    
    if(!checkForgotEmail(mail)) {
        alert("Check entered mail address format!");
    }
    else {

        $.ajax({type: "GET",
            url: serviceURL + 'forgotPassword.php',
            dataType : 'json',
            data: {'mail': mail},

            success:function(result) {
                alert("Success, mail sent: " + result)

            },

            error: function (result) {
                alert("error on mail send: " + result); 
            }

        });
    }
}

